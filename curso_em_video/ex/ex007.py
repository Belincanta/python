n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
som = n1+n2
sub = n1-n2
div = n1/n2
mul = n1*n2
print('A soma dos valores é {}, a subtração entre eles é {}, a subtração é {:.2f} e por fim, a multiplação é {}'.format(som, sub, div, mul))

